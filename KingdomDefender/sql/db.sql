DROP TABLE K_MONSTER;
DROP TABLE K_MONSTER_CHARACTER;
DROP TABLE K_PATH;
DROP TABLE K_TOWER_CHARACTER;


CREATE TABLE K_MAP (
K_PK INTEGER PRIMARY KEY,
K_LEVEL INTEGER,
K_WIDTH INTEGER,
K_HEIGHT INTEGER,
K_WAVE_NUM INTEGER
);


CREATE TABLE K_PATH (
K_PK INTEGER PRIMARY KEY,
K_MAP_LEVEL INTEGER,
K_COLUMN INTEGER,
K_ROW INTEGER,
K_NEXT_PATH INTEGER
);

CREATE TABLE K_MONSTER_CHARACTER (
K_PK INTEGER PRIMARY KEY,
K_NAME VARCHAR,
K_DAMAGE INTEGER,
K_REWARD DOUBLE,
K_HP INTEGER,
K_SPRITE_FILE_NAME VARCHAR,
K_MATRIX_WIDTH INTEGER,
K_MATRIX_HEIGHT INTEGER
);

CREATE TABLE K_MONSTER (
K_PK INTEGER PRIMARY KEY,
K_MAP_LEVEL INTEGER,
K_WAVE INTEGER,
K_MOVE_SPEED INTEGER,
K_CHARACTER INTEGER
);

CREATE TABLE K_TOWER_CHARACTER (
