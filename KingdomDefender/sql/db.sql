K_AREA_OF_EFFECT INTEGER,
K_RANGE INTEGER,
K_ATTACK_SPEED FLOAT,
K_BUY_COST INTEGER,
K_SELL_COST INTEGER,
K_SPRITE_FILE_NAME VARCHAR,
K_SPRITE_IMAGE_WIDTH INTEGER,
K_SPRITE_IMAGE_HEIGHT INTEGER,
K_SPRITE_PTILE_COLUMN INTEGER,
K_SPRITE_PTILE_ROW INTEGER,
K_MATRIX_WIDTH INTEGER,
K_MATRIX_HEIGHT INTEGER
);

